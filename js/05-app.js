// 5. Do While Loop

let i = 100;

do {// se ejecuta al menos una vez, no importa si se cumple o no la condición
    console.log(`Número ${i}`);
    i++;
} while(i < 10);// 100 < 10 no se cumple la condición.
// aún así el resultado es Número 100

let ii = 0;

do {// se ejecuta al menos una vez, no importa si se cumple o no la condición
    console.log(`Segundo whilw ${ii}`);
    ii++;
} while(ii < 10);// 100 < 10 no se cumple la condición.
